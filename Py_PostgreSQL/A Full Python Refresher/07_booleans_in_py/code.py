# Value that can be either true or false
# Comparisons:
    # == (equal/equal)
    # != (not equal)
    # > (greater than)
    # < (less than)
    # >= (greater than and equal to)
    # <= (less than and equal to)
# Can be used anywhere but have different response
print( 5 == 5)
print(5 > 5)
print(10 != 10)

